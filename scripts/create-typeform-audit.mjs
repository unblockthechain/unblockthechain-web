#!/usr/bin/env node
/**
 * Creates a Typeform via Create API and writes lib/audit-form-url.ts
 *
 * 1. Create a personal access token: https://admin.typeform.com/account#/section/tokens
 * 2. Run:
 *      export TYPEFORM_ACCESS_TOKEN="tfp_..."
 *      npm run create:audit-form
 * 3. Commit lib/audit-form-url.ts and deploy. Set the same URL in Netlify env if you prefer env over repo.
 *
 * Docs: https://developer.typeform.com/create/reference/create-form/
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const outFile = path.join(root, "lib", "audit-form-url.ts");

const token = process.env.TYPEFORM_ACCESS_TOKEN?.trim();
if (!token) {
  console.error(
    "Missing TYPEFORM_ACCESS_TOKEN.\n" +
      "Create one at https://admin.typeform.com/account#/section/tokens then:\n" +
      "  export TYPEFORM_ACCESS_TOKEN='tfp_...'\n" +
      "  npm run create:audit-form"
  );
  process.exit(1);
}

const body = {
  title: "Unblock the Chain — Security audit request",
  type: "quiz",
  settings: {
    is_public: true,
    language: "en",
  },
  fields: [
    { type: "short_text", title: "Full name" },
    { type: "email", title: "Work email" },
    { type: "short_text", title: "Company / organization" },
    {
      type: "long_text",
      title:
        "What do you need? (e.g. smart contract audit, RWA, tokenization, other)",
    },
    {
      type: "long_text",
      title: "Brief description of your project or protocol",
    },
  ],
};

const res = await fetch("https://api.typeform.com/forms", {
  method: "POST",
  headers: {
    Authorization: `Bearer ${token}`,
    "Content-Type": "application/json",
  },
  body: JSON.stringify(body),
});

const json = await res.json().catch(() => ({}));

if (!res.ok) {
  console.error("Typeform API error", res.status, JSON.stringify(json, null, 2));
  process.exit(1);
}

const id = json.id;
if (!id || typeof id !== "string") {
  console.error("Unexpected response (no form id):", JSON.stringify(json, null, 2));
  process.exit(1);
}

const publicUrl = `https://form.typeform.com/to/${id}`;

const ts = `/**
 * Public Typeform URL for “Request audit” (no secret here).
 * Generated by scripts/create-typeform-audit.mjs — do not edit by hand.
 */
export const AUDIT_FORM_URL_FROM_REPO = ${JSON.stringify(publicUrl)};
`;

fs.writeFileSync(outFile, ts, "utf8");

console.log("\nCreated Typeform:", publicUrl);
console.log("\nWrote", path.relative(root, outFile));
console.log("\nNext: commit lib/audit-form-url.ts, push, and redeploy.");
console.log(
  "Optional: set NEXT_PUBLIC_AUDIT_FORM_URL in Netlify to the same URL (or rely on the file).\n"
);
